// Program to check whether the number is even or odd

class OddOrEven
{
	public static void main(String args[])
	{
		int num = 246;
		if(num%2==0)
		{
			System.out.println("Number "+num+" is an Even number");
		}
		else
		{
			System.out.println("Number "+num+" is an Odd number");
		}
	}
}
		