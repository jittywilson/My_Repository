class vowels
{
	public static void main(String args[])
	{
		char c = 'm';
		switch(c)
		{
			case 'a':
			System.out.println("Character entered is Vowel");
			break;
			case 'e':
			System.out.println("Character entered is Vowel");
			break;
			case 'i':
			System.out.println("Character entered is Vowel");
			break;
			case 'o':
			System.out.println("Character entered is Vowel");
			break;
			case 'u':
			System.out.println("Character entered is Vowel");
			break;
			default:
			System.out.println("Character entered is not a Vowel");
		}
	}
}
			
			