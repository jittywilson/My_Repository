// program to find area of rectangle

class Area
{
	public static void main(String args[])
	{
		float length = 10f;
		float breadth = 20f;
		double area;
		area = 0.5*length*breadth;
		System.out.println("Area of rectangle is "+area);
	}
}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	